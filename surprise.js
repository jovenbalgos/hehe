function changeImage() {
    const image = document.getElementById('image');
    const image2 =document.getElementById('image2');
    const image3 =document.getElementById('image3');
    const card1 = 'card.png'; // Path to the first image
    const card2 = 'card1.png'; // Path to the second image
    const textelement = document.getElementById('next1');
    const buttonnext1 = document.getElementById('next_button1');
    // Toggle between the two images
    if (image.src.includes(image)) {
        image.src = card1;
    } else {
        image.src = card2;
    }
    textelement.style.display="block";
    image2.style.display = "none";
    image3.style.display = "none";
    buttonnext1.style.display = "block";

}
